package com.iwhere.pathfinding.dto;

public class GPSPair {
	
	GPSPoint start;
	GPSPoint end;
	public GPSPoint getStart() {
		return start;
	}
	public void setStart(GPSPoint start) {
		this.start = start;
	}
	public GPSPoint getEnd() {
		return end;
	}
	public void setEnd(GPSPoint end) {
		this.end = end;
	}

}
